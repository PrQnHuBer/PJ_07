#non primitve data type / collection / data structure ข้อมูลชนิดไม่พื้นฐาน

# non primitve data type 
#number ,boolean ,string 

#collection
#List, Tuple ,Set , Dictionary

#data type จะเอามาใช้กับเขียนโปรแกรมในเรื่องของ variable parameter funcion method

#  List  คือข้อมูลหลายข้อมูล คนละชนิด ซ้ำกันได้
        #0  1   2  3    4        5  
List = [10,20,True,10,'SAU',[20,'IOT']]
        #6  5   4   3   2        1    คือ -    

# Tuple  คือข้อมูลหลายข้อมูล คนละชนิด ซ้ำกันได้ แต่แก้ไม่ได้ เพิ่มไม่ได้ลดไม่ได้
        #0  1   2  3    4        5  
Tuple = (10,20,True,10,'SAU',(20,'IOT'))
        #6  5   4   3   2        1    คือ -

# Set  คือข้อมูลหลายข้อมูล คนละชนิด ซ้ำกันไม่ได้(ซ้ำที่เป็นตัวเดียวกัน) ไม่มีลำดับ แก้ไขไม่ได้ เพิ่มได้ลดได้ ข้อมูลที่อยู่ใน Set ได้มีแค่ Tuple

Set = {10,20,True,10,'SAU',(20,'IOT')}

# Dictionary  คือข้อมูลหลายข้อมูล ที่เป็น key value แก้ไขได้ ไม่มีลำดับ ซ้ำได้
#key เป็น string integer Value เป็นอะไรก็ได้

Dictionary = {'name':'mod','age':32,555:999,'flag':True,'wow':[10,20,30]}